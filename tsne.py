def get_info(network, loader, device):
    feature_list = np.array([])
    label_list = np.array([])

    network.eval()
    with torch.no_grad():
        for x, y in tqdm(loader):
            x = x.to(device)
            label_list = np.append(label_list, np.array(y))
            feature = network.K_encoder(x)
            # feature = network.network[0](x)
            np_feature = feature.cpu().numpy()
            feature_list = np.append(feature_list, np_feature)
            
    label_list = label_list.reshape(-1,1)
    bs = label_list.shape[0]
    feature_list = feature_list.reshape(bs,-1)
    return feature_list, label_list

def get_T_SNE(feature,label,dataset,num,name):
    print('begin!!!!')

    embeddings = TSNE(n_jobs=4).fit_transform(feature)
    vis_x = embeddings[:, 0]
    vis_y = embeddings[:, 1]
    label = [a[0] for a in label]
    # plt.title(name)
    # plt.axis('off')
    plt.scatter(vis_x, vis_y, c=label, cmap=plt.cm.get_cmap("jet", num), marker='.')
    plt.colorbar(ticks=range(num))
    plt.clim(-0.5, num-0.5)
    plt.savefig(f'figs/pdf_{dataset}/{name}.pdf',dpi=600,bbox_inches='tight')

